#include ".\tp_HandShake_cfg.h"
#include ".\tp_HandShake_target.h"

/*********************************************************************************************************
  端口定义
*********************************************************************************************************/
static const TP_HANDSHAKE_INFO __GtpiHandShake = {                      /*  端口信息                    */
    {
        (TM_PORT_FUNC *)&GtpfHandShake,
        0xff,
        TRUE
    }
};

/*********************************************************************************************************
** Function name:           tpHandShakeInit
** Descriptions:            
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpHandShakeInit (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortAdd((TM_PORT_INFO *)&__GtpiHandShake);
}

/*********************************************************************************************************
** Function name:           tpHandShakeDel
** Descriptions:            端口卸载
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpHandShakeDel (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortDel((TM_PORT_INFO *)&__GtpiHandShake);
}


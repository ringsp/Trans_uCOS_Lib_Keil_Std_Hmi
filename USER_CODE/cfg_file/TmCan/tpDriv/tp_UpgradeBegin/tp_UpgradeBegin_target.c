#include ".\tp_UpgradeBegin_cfg.h"
#include ".\tp_UpgradeBegin_target.h"

/*********************************************************************************************************
  端口定义
*********************************************************************************************************/
static const TP_UPGRADEBEGIN_INFO __GtpiUpgradeBegin = {                /*  端口信息                    */
    {
        (TM_PORT_FUNC *)&GtpfUpgradeBegin,
        0x00,
        TRUE
    }
};

/*********************************************************************************************************
** Function name:           tpUpgradeBeginInit
** Descriptions:            UpgradeBegin端口初始化
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpUpgradeBeginInit (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortAdd((TM_PORT_INFO *)&__GtpiUpgradeBegin);
}
/*********************************************************************************************************
** Function name:           tpUpgradeBeginDel
** Descriptions:            UpgradeBegin端口卸载
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpUpgradeBeginDel (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortDel((TM_PORT_INFO *)&__GtpiUpgradeBegin);
}

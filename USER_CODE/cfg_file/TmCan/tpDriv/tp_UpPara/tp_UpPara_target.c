#include ".\tp_UpPara_cfg.h"
#include ".\tp_UpPara_target.h"

/*********************************************************************************************************
  端口定义
*********************************************************************************************************/
static const TP_UPPARA_INFO __GtpiUpPara = {                            /*  端口信息                    */
    {
        (TM_PORT_FUNC *)&GtpfUpPara,
        0x06,
        TRUE
    }
};

/*********************************************************************************************************
** Function name:           tpUpParaInit
** Descriptions:            
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpUpParaInit (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortAdd((TM_PORT_INFO *)&__GtpiUpPara);
}

/*********************************************************************************************************
** Function name:           tpUpParaDel
** Descriptions:            端口卸载
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpUpParaDel (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortDel((TM_PORT_INFO *)&__GtpiUpPara);
}


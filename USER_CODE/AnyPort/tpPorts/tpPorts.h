/*********************************************************************************************************
  设备通用功能端口基地址
*********************************************************************************************************/
#define  TP_STOP_PORT_ADDR      0x00                                    /*  急停端口地址                */

typedef enum port_addr {
  __TP_KEY              = 0x00,                                         /*  按键                        */    
  __TP_PRINTF           = 0x01,                                         /*  GUI_Printf                  */
  __TP_CLR_SCR          = 0x02,                                         /*  GUI_ClearSCR                */
  __TP_CLR_RECT         = 0x03,                                         /*  GUI_ClearRectangle          */


  __TP_LED_ON           = 0x06,                                         /*  led on                      */
  __TP_LED_OFF          = 0x07,                                         /*  led off                     */
  __TP_LED_BLINK        = 0x08,                                         /*  led blink                   */
  __TP_PROG             = 0x09,                                         /*  程序删除/发布               */
} PORT_ADDR;





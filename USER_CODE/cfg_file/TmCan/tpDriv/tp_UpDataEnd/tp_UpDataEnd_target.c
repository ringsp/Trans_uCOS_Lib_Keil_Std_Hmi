#include ".\tp_UpDataEnd_cfg.h"
#include ".\tp_UpDataEnd_target.h"

/*********************************************************************************************************
  端口定义
*********************************************************************************************************/
static const TP_UPDATA_INFO __GtpiUpDataEnd = {                         /*  端口信息                    */
    {
        (TM_PORT_FUNC *)&GtpfUpDataEnd,
        0x03,
        TRUE
    }
};

/*********************************************************************************************************
** Function name:           tpUpDataEndInit
** Descriptions:            
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpUpDataEndInit (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortAdd((TM_PORT_INFO *)&__GtpiUpDataEnd);
}

/*********************************************************************************************************
** Function name:           tpUpDataEndDel
** Descriptions:            端口卸载
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpUpDataEndDel (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortDel((TM_PORT_INFO *)&__GtpiUpDataEnd);
}


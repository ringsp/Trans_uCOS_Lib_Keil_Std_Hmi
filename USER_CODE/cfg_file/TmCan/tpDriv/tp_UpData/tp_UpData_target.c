#include ".\tp_UpData_cfg.h"
#include ".\tp_UpData_target.h"

/*********************************************************************************************************
  端口定义
*********************************************************************************************************/
static const TP_UPDATA_INFO __GtpiUpData = {                        /*  端口信息                    */
    {
        (TM_PORT_FUNC *)&GtpfUpData,
        0x01,
        TRUE
    }
};

/*********************************************************************************************************
** Function name:           tpUpDataInit
** Descriptions:            
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpUpDataInit (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortAdd((TM_PORT_INFO *)&__GtpiUpData);
}

/*********************************************************************************************************
** Function name:           tpUpDataDel
** Descriptions:            端口卸载
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpUpDataDel (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortDel((TM_PORT_INFO *)&__GtpiUpData);
}


#include ".\tp_ApplyUsr_cfg.h"
#include ".\tp_ApplyUsr_target.h"

/*********************************************************************************************************
  端口定义
*********************************************************************************************************/
static const TP_APPLYUSR_INFO __GtpiApplyUsr = {               /*  端口信息                    */
    {
        (TM_PORT_FUNC *)&GtpfApplyUsr,
        0x05,
        TRUE
    }
};

/*********************************************************************************************************
** Function name:           tpApplyUsrInit
** Descriptions:            
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpApplyUsrInit (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortAdd((TM_PORT_INFO *)&__GtpiApplyUsr);
}

/*********************************************************************************************************
** Function name:           tmPortApplyUsrDel
** Descriptions:            端口卸载
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpApplyUsrDel (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortDel((TM_PORT_INFO *)&__GtpiApplyUsr);
}


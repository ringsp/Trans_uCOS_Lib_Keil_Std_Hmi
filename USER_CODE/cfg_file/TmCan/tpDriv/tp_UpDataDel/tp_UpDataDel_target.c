#include ".\tp_UpDataDel_cfg.h"
#include ".\tp_UpDataDel_target.h"

/*********************************************************************************************************
  端口定义
*********************************************************************************************************/
static const TP_UPDATA_INFO __GtpiUpDataDel = {                 /*  端口信息                    */
    {
        (TM_PORT_FUNC *)&GtpfUpDataDel,
        0x02,
        TRUE
    }
};

/*********************************************************************************************************
** Function name:           tpUpDataDelInit
** Descriptions:            
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpUpDataDelInit (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortAdd((TM_PORT_INFO *)&__GtpiUpDataDel);
}

/*********************************************************************************************************
** Function name:           tpUpDataDelDel
** Descriptions:            端口卸载
** input parameters:        none
** output parameters:       none
** Returned value:          none
*********************************************************************************************************/
void tpUpDataDelDel (void)
{ 
    /*
     *  添加端口
     */
    tmDevicePortDel((TM_PORT_INFO *)&__GtpiUpDataDel);
}

